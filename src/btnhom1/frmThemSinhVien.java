    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed

        if (!txtHoTen.getText().trim().isEmpty()) {
            if (!txtLop.getText().trim().isEmpty()) {
                if (!txtMaSV.getText().trim().isEmpty()) {
                    if (!txtMatKhau.getPassword().equals("")) {
                        //không có trường nào trống
                        String gioiTinh = "Nam";
                        if (sltNu.isSelected()) {
                            gioiTinh = "Nữ";
                        }
//                         System.out.println(sltNam.isSelected());
//                         System.out.println(cbNganh.getModel().getSelectedItem());
                        int nganh = Integer.parseInt(cbNganh.getModel().getSelectedItem().toString().substring(0, 1));
                        SinhVien sv = new SinhVien(
                                txtMaSV.getText(),
                                Helper.getMD5(String.copyValueOf(txtMatKhau.getPassword())),
                                txtHoTen.getText().trim(),
                                gioiTinh,
                                Integer.parseInt(cbTuoi.getModel().getSelectedItem().toString()),
                                nganh
                        );
                        qlsv.addSinhVien(sv);
                        try {
                            qlsv.ghi();
                            this.reset();
                            JOptionPane.showMessageDialog(null, "Thêm thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                        } catch (IOException ex) {
                            Logger.getLogger(frmThemSinhVien.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        return;
                    }
                }
            }
        }
        JOptionPane.showMessageDialog(null, "Nhập thiếu trường", "Lỗi", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_btnThemActionPerformed

