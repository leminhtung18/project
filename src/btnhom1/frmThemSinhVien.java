    private void setMaSV() {
        arrSVs = qlsv.getArrSVs();
        if (arrSVs == null) {
            maSV = "SV00001";
        } else {
            String maLastSV = arrSVs[arrSVs.length - 1].getMaSV();
            maSV = (Helper.tangKey(maLastSV));
        }
    }

    private void myinit() throws IOException, ClassNotFoundException {
        qlsv = new QLSinhVien();
        this.setMaSV();
        txtMaSV.setText(maSV);
        QLNganh qlnganh = null;
        try {
            qlnganh = new QLNganh();
        } catch (IOException | ClassNotFoundException ex) {
            Logger.getLogger(frmThemSinhVien.class.getName()).log(Level.SEVERE, null, ex);
        }
        DefaultComboBoxModel listTuoi = new DefaultComboBoxModel();
        DefaultComboBoxModel listNganh = new DefaultComboBoxModel();
        for (int i = 18; i <= 99; i++) {
            listTuoi.addElement(i);
        }
        cbTuoi.setModel(listTuoi);
        for (String maNganh : qlnganh.getMaNganh()) {
            listNganh.addElement(maNganh);
        }
        cbNganh.setModel(listNganh);
    }

